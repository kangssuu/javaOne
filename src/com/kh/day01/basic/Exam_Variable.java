package com.kh.day01.basic;

public class Exam_Variable {
	public static void main(String [] args) {
		
		final double PI = 3.14;
		
		double redius = 10.0;
		double circleArea = redius * redius * PI;
		//원의 면적 출력
		System.out.println("원의면적 : " + circleArea);
		
		char a = 'A';
		char b = 'B';
		System.out.println(a+b);
	}
}
